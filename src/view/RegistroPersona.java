    public void cargar(String valor) {
        DefaultTableModel modelo = new DefaultTableModel();

        int id_tipo = 0;
        String mostrar = "SELECT * FROM usuarios WHERE CONCAT(id_usuario,nombres,apellidos, email, telefono) LIKE '%" + valor + "%'";

        String[] Titulos = {"Código", "Nombres", "Apelidos", "Email", "Télefono", "Tipo usuario"};
        modelo.setColumnIdentifiers(Titulos);
        this.tb_usuarios.setModel(modelo);

        Conexion j = new Conexion();
        j.conectar();
        sql = j.sql;

        try {

            String[] registros = new String[6];
            s = sql.createStatement();
            ResultSet rs = s.executeQuery(mostrar);
            while (rs.next()) {

                registros[0] = rs.getString("id_usuario");
                registros[1] = rs.getString("nombres");
                registros[2] = rs.getString("apellidos");
                registros[3] = rs.getString("email");
                registros[4] = rs.getString("telefono");
                id_tipo = rs.getInt("id_tipo_usuario");

                switch (id_tipo) {
                    case 1:
                        registros[5] = "Administrador";
                        break;
                    case 2:
                        registros[5] = "Uusario";
                        break;
                }

                modelo.addRow(registros);
            }
            tb_usuarios.setModel(modelo);
        } catch (SQLException ex) {
            Logger.getLogger(RegistroPreguntas.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

