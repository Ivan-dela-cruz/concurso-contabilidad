private void txt_nombreKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txt_nombreKeyTyped
// TODO add your handling code here:
    char car = evt.getKeyChar();
    if ((car < 'a' || car > 'z') && (car < 'A' || car > 'Z') && (car != (char) KeyEvent.VK_SPACE)) {
        evt.consume();
    }
}//GEN-LAST:event_txt_nombreKeyTyped

private void mneliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_mneliminarActionPerformed
// TODO add your handling code here:
    int fila = tb_usuarios.getSelectedRow();

    String codigo = "";
    codigo = tb_usuarios.getValueAt(fila, 0).toString();
    if (fila >= 0) {
        try {
            Conexion j = new Conexion();
            j.conectar();
            String sql = "DELETE FROM usuarios WHERE id_usuario='" + codigo + "'";
            j.eliminar(sql);

            cargar("");
            bloquear();
            limpiar();
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(null, ex);
        }

    } else {
        JOptionPane.showMessageDialog(this, "No ha selecionada ninguna fila");
    }


}//GEN-LAST:event_mneliminarActionPerformed

