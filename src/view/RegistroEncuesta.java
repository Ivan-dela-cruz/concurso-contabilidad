    void cargar(String valor) {
        DefaultTableModel modelo = new DefaultTableModel();

        //SELECT id, codigo, categoria, titulo, descripcion, estado, tiempo,fecha_inicio, fecha_final FROM encuestas WHERE id = 1
        String mostrar = "SELECT * FROM encuestas WHERE CONCAT(codigo, categoria,nivel, titulo, descripcion, estado,tiempo, fecha_inicio, fecha_final) LIKE '%" + valor + "%'";

        String[] Titulos = {"CÓDIGO", "CATEGORÍA", "NIVEL", "TITULO", "INDICACIONES", "ESTADO", "TIEMPO", "INICIO", "FIN"};
        modelo.setColumnIdentifiers(Titulos);
        this.tbclientes.setModel(modelo);

        Conexion j = new Conexion();
        j.conectar();
        sql = j.sql;

        try {

            String[] registros = new String[9];
            s = sql.createStatement();
            ResultSet rs = s.executeQuery(mostrar);
            while (rs.next()) {
                //SELECT id, codigo, categoria, titulo, descripcion, estado, tiempo,fecha_inicio, fecha_final
                registros[0] = rs.getString("codigo");
                registros[1] = rs.getString("categoria");
                registros[2] = rs.getString("nivel");
                registros[3] = rs.getString("titulo");
                registros[4] = rs.getString("descripcion");
                if (rs.getString("estado").equals("1")) {
                    registros[5] = "PUBLICADO";
                } else {
                    registros[5] = "BORRADOR";
                }

                registros[6] = rs.getString("tiempo");
                registros[7] = rs.getString("fecha_inicio");
                registros[8] = rs.getString("fecha_final");
                modelo.addRow(registros);
            }
            tbclientes.setModel(modelo);
        } catch (SQLException ex) {
            Logger.getLogger(RegistroEncuesta.class.getName()).log(Level.SEVERE, null, ex);
        }

    }

