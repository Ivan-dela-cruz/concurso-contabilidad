private void btnguardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnguardarActionPerformed
// TODO add your handling code here:

    DateManager dateManager = new DateManager(fecha_inicio, fecha_fin);
    String cod, categoria, nivel, titulo, indiciones, estado, fecha_inicio, fecha_final;
    int estado_encuesta = 0, tiempo;
    cod = txtcod.getText().toUpperCase();
    categoria = cb_categoria.getSelectedItem().toString();
    nivel = cb_nivel.getSelectedItem().toString();
    titulo = txt_titulo.getText().toUpperCase();
    indiciones = text_indicaciones.getText().toUpperCase();
    estado = combo_estado.getSelectedItem().toString();
    tiempo = (Integer) spiner_minutos.getValue();
    fecha_inicio = dateManager.getFechaInicio();
    fecha_final = dateManager.getFechaFin();
    if (estado.equals("PUBLICADO")) {
        estado_encuesta = 1;
    }

    try {
        Conexion j = new Conexion();
        j.conectar();

        //`id`, `codigo`, `categoria`,nivel,`titulo`, `descripcion`, `estado`, `fecha_inicio`, `fecha_final` FROM `encuestas`
        String sql = "INSERT INTO encuestas(codigo,categoria,nivel,titulo,descripcion,estado,tiempo,fecha_inicio,fecha_final) VALUES('"
                + cod + "','"
                + categoria + "','"
                + nivel + "','"
                + titulo + "','"
                + indiciones + "','"
                + estado_encuesta + "','"
                + tiempo + "','"
                + fecha_inicio + "','"
                + fecha_final + "')";
        j.insertar(sql);
        System.out.println("EXITO");
        JOptionPane.showMessageDialog(null, "Guardado exitosamente");
        limpiar();
        cargar("");
        bloquear();
    } catch (Exception e) {
        System.out.println("ERROR");
    }
}//GEN-LAST:event_btnguardarActionPerformed

