private void btnactualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnactualizarActionPerformed
// TODO add your handling code here:
    try {
        int estado = 0, tiempo;
        DateManager dateManager = new DateManager(fecha_inicio, fecha_fin);
        Conexion j = new Conexion();
        j.conectar();

        if (combo_estado.getSelectedItem().toString().equals("PUBLICADO")) {
            estado = 1;
        }
        tiempo = (Integer) spiner_minutos.getValue();

        //codigo,categoria,nivel,titulo,descripcion,estado,tiempo,fecha_inicio,fecha_final
        j.editar("UPDATE encuestas SET codigo='" + txtcod.getText().toUpperCase()
                + "',categoria='" + cb_categoria.getSelectedItem().toString().toUpperCase()
                + "',nivel='" + cb_nivel.getSelectedItem().toString().toUpperCase()
                + "',titulo='" + txt_titulo.getText().toString().toUpperCase()
                + "',descripcion='" + text_indicaciones.getText().toUpperCase()
                + "',estado='" + estado
                + "',tiempo='" + tiempo
                + "',fecha_inicio='" + dateManager.getFechaInicio()
                + "',fecha_final='" + dateManager.getFechaFin()
                + "' WHERE id='" + id_encontrado + "'");

        cargar("");
        bloquear();
        limpiar();
    } catch (Exception e) {
        System.out.print(e.getMessage());
    }

}//GEN-LAST:event_btnactualizarActionPerformed

