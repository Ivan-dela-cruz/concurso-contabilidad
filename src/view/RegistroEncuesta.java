private void mneliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_mneliminarActionPerformed
// TODO add your handling code here:
    int fila = tbclientes.getSelectedRow();
   
    String codigo = "";
    codigo = tbclientes.getValueAt(fila, 0).toString();
    if (fila >= 0) {
        try {
            Conexion j = new Conexion();
            j.conectar();
            String sql = "DELETE FROM encuestas WHERE codigo='" + codigo + "'";
            j.eliminar(sql);

            cargar("");
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(null, ex);
        }
    } else {
        JOptionPane.showMessageDialog(this, "No ha selecionada ninguna fila");
    }

}//GEN-LAST:event_mneliminarActionPerformed

private void mnmodificarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_mnmodificarActionPerformed
// "CÓDIGO", "CÉDULA", "NOMBRES", "APELLIDOS", "DIRECCIÓN", "TELEFONO", "PLACA", "MODELO", "COLOR"    
    desbloquear();
    btnactualizar.setEnabled(true);
    int filamodificar = tbclientes.getSelectedRow();

    if (filamodificar >= 0) {
        txtcod.setText(tbclientes.getValueAt(filamodificar, 0).toString());
        cb_categoria.setSelectedItem(tbclientes.getValueAt(filamodificar, 1).toString());
        cb_nivel.setSelectedItem(tbclientes.getValueAt(filamodificar, 2).toString());
        txt_titulo.setText(tbclientes.getValueAt(filamodificar, 3).toString());
        text_indicaciones.setText(tbclientes.getValueAt(filamodificar, 4).toString());
        combo_estado.setSelectedItem(tbclientes.getValueAt(filamodificar, 5).toString());
        spiner_minutos.setValue(Integer.parseInt(tbclientes.getValueAt(filamodificar, 6).toString()));
        fecha_inicio.setDate(Date.valueOf(tbclientes.getValueAt(filamodificar, 7).toString()));
        fecha_fin.setDate(Date.valueOf(tbclientes.getValueAt(filamodificar, 7).toString()));

        Conexion conexion = new Conexion();
        conexion.conectar();
        id_encontrado = conexion.buscarId("SELECT id FROM encuestas WHERE codigo = '" + tbclientes.getValueAt(filamodificar, 0).toString() + "'");

    } else {
        JOptionPane.showMessageDialog(this, "No ha seleccionado ");
    }

}//GEN-LAST:event_mnmodificarActionPerformed

